# Here are are going to lear about comparison using different signs.

X = 55
Y = 55

if X == Y:                    # Here == is used to check whether X is equal to Y or not
    print("They are equal.")
else:
    print("They are not equal.")      


A = 23
B = 27

if A >= B:                  # Here >= is used to check that which value is bigger, and similarly we can do with <= sign.
    print("Here A is greater than B.")
else:
    print("A is less than B.")   


if A <= B:                  # Here <= is used to check that which value is bigger.
    print("Here A is less than B.")
else:
    print("A is greater than B.")